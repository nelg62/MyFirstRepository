function addFun(a, b) {
  console.log(a + b);
}

function minusFun(a, b) {
  console.log(a - b);
}

function divideFun(a, b) {
  console.log(a / b);
}

function timesFun(a, b) {
  console.log(a * b);
}

function personName(name) {
  console.log(`Hello ${name}`);
}

addFun(5, 5);

minusFun(5, 5);

divideFun(5, 5);

timesFun(5, 5);

personName("Glen");
